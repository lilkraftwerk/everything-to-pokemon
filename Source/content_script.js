function handleText(textNode)
{
	if (textNode.parentElement.tagName.toLowerCase() === "script" || textNode.parentElement.isContentEditable === true) {
		return false;
	}

	var oldValue = textNode.nodeValue;
	var v = oldValue;

	// adlibs
	// v = v.replace(/(\w{3,})([.!?;])/ig, "$1," + " " + getCatchphrase() + "$2");

	var splitNode = v.split(" ")
	for(var i = 0; i < splitNode.length; i++){
		splitNode[i] = splitNode[i].replace(/(\b\w+\b)/ig, getCatchphrase());
	}
	if (v !== splitNode.join(" ")) {
		textNode.nodeValue = splitNode.join(" ");
	}
}

